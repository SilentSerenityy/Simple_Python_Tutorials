x = 7
y = 9
# two random numbers that I like

print('Addition:\t\t', x, '+', y, '=', x + y)
# add : x + y

print('\nSubtraction:\t\t', x, '-', y , '=', x - y)
# subtract : x - y

print('\nMultiplication:\t\t', x, 'x', y, '=', x * y)
# multiply : x * y

print('\nDivision:\t\t', x, '÷', y , '=', x / y)
# divide : x / y

print('\nFloor Division:\t\t', x, '÷', y, '=', x // y)
# floor divide (returns largest possible int "⌊ ⌋") : x // y

print('\nRemainder\t\t', x, '%', y , '=', x % y)
# the remainder : x % y
 
print('\nExponent:\t\t', x, '² = ', x ** y)
# exponents : x ** y
